@Configuration
public class KafkaConfig {
    // 创建一个名为testtopic的Topic并设置分区数为8，分区副本数为2
    @Bean
    public NewTopic initialTopic() {
        return new NewTopic("topic1",8, (short) 2 );
    }

}
